package com.sparrow.server.base.command;

public class LoginInfo {
	private String user;
	private String error;

	public String getUser() {
		return user;
	}

	public void setUser(String user) {
		this.user = user;
	}

	public String getError() {
		return error;
	}

	public void setError(String error) {
		this.error = error;
	}
}

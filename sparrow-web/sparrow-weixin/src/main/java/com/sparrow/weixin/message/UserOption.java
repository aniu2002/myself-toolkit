package com.sparrow.weixin.message;

public class UserOption {

	private String openId;
	private String roleIndex;
	private String topCategory;
	private String secondCatgory;
	private String location;
	
	public String getOpenId() {
		return openId;
	}
	public void setOpenId(String openId) {
		this.openId = openId;
	}
	public String getRoleIndex() {
		return roleIndex;
	}
	public void setRoleIndex(String roleIndex) {
		this.roleIndex = roleIndex;
	}
	public String getTopCategory() {
		return topCategory;
	}
	public void setTopCategory(String topCategory) {
		this.topCategory = topCategory;
	}
	public String getSecondCatgory() {
		return secondCatgory;
	}
	public void setSecondCatgory(String secondCatgory) {
		this.secondCatgory = secondCatgory;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	
}

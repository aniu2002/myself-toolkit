package com.sparrow.weixin.message.category;

public class PopularCategory {

    private String chooseIndex;
    private String categoryIndex;
    private String categoryName;
    private String categoryPicUrl;

    public String getChooseIndex() {
        return chooseIndex;
    }

    public void setChooseIndex(String chooseIndex) {
        this.chooseIndex = chooseIndex;
    }

    public String getCategoryIndex() {
        return categoryIndex;
    }

    public void setCategoryIndex(String categoryIndex) {
        this.categoryIndex = categoryIndex;
    }

    public String getCategoryName() {
        return categoryName;
    }

    public void setCategoryName(String categoryName) {
        this.categoryName = categoryName;
    }

    public String getCategoryPicUrl() {
        return categoryPicUrl;
    }

    public void setCategoryPicUrl(String categoryPicUrl) {
        this.categoryPicUrl = categoryPicUrl;
    }
}
